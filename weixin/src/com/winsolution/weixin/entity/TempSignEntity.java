package com.winsolution.weixin.entity;

import java.sql.Date;

public class TempSignEntity extends PageEntity {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int rownum;
	// 用户ID
	private String uid;
	// 签到签退类型：1-签到;2-签退
	private String signtype;
	// 消息时间（元历到秒）
	private String msgtime;
	// 记录所属年月
	private String signyyyymm;
	// 记录日期
	private Date signdate;
	// 消息来源：1-微信;2-Android客户端;3-IOS客户端;4-PC客户端;0-未知
	private String msgfrom;
	// 事件类型
	private String ent;
	// 纬度（该值与经度精度组合且与地点名称至少二选一）
	private float latitude;
	// 经度（该值与纬度精度组合且与地点名称至少二选一）
	private float longitude;
	// 精度（该值与纬度经度组合且与地点名称至少二选一）precision
	private float prec;
	// 地点或建筑物名称（该值与经纬度精度至少二选一）
	private String posname;
	// 应用ID
	private int appid;

	// 查询条件 开始时间
	private Date startdate;
	// 查询条件 结束时间
	private Date enddate;

	public int getRownum() {
		return rownum;
	}

	public void setRownum(int rownum) {
		this.rownum = rownum;
	}

	private TempInfoEntity tempInfo;

	public TempInfoEntity getTempInfo() {
		return tempInfo;
	}

	public void setTempInfo(TempInfoEntity tempInfo) {
		this.tempInfo = tempInfo;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getSigntype() {
		return signtype;
	}

	public void setSigntype(String signtype) {
		this.signtype = signtype;
	}

	public String getMsgtime() {
		return msgtime;
	}

	public void setMsgtime(String msgtime) {
		this.msgtime = msgtime;
	}

	public String getSignyyyymm() {
		return signyyyymm;
	}

	public void setSignyyyymm(String signyyyymm) {
		this.signyyyymm = signyyyymm;
	}

	public Date getSigndate() {
		return signdate;
	}

	public void setSigndate(Date signdate) {
		this.signdate = signdate;
	}

	public String getMsgfrom() {
		return msgfrom;
	}

	public void setMsgfrom(String msgfrom) {
		this.msgfrom = msgfrom;
	}

	public String getEnt() {
		return ent;
	}

	public void setEnt(String ent) {
		this.ent = ent;
	}

	public float getLatitude() {
		return latitude;
	}

	public void setLatitude(float latitude) {
		this.latitude = latitude;
	}

	public float getLongitude() {
		return longitude;
	}

	public void setLongitude(float longitude) {
		this.longitude = longitude;
	}

	public String getPosname() {
		return posname;
	}

	public void setPosname(String posname) {
		this.posname = posname;
	}

	public float getPrec() {
		return prec;
	}

	public void setPrec(float prec) {
		this.prec = prec;
	}

	public int getAppid() {
		return appid;
	}

	public void setAppid(int appid) {
		this.appid = appid;
	}

	public Date getStartdate() {
		return startdate;
	}

	public void setStartdate(Date startdate) {
		this.startdate = startdate;
	}

	public Date getEnddate() {
		return enddate;
	}

	public void setEnddate(Date enddate) {
		this.enddate = enddate;
	}

}
